CREATE TABLE proveedores (
    id_prov NUMBER GENERATED BY DEFAULT AS IDENTITY, -- Identificador único automático
    nombre_prov VARCHAR2(100) NOT NULL, -- Nombre del proveedor
    nit_prov VARCHAR2(20) NOT NULL, -- NIT del proveedor
    telefono_prov VARCHAR2(15), -- Teléfono del proveedor
    correo_prov VARCHAR2(100), -- Correo electrónico del proveedor
    categorias_prov VARCHAR2(50), -- Categoría del proveedor
    codigo_validacion VARCHAR2(10), -- Código de validación
    PRIMARY KEY (id_prov) -- Llave primaria
);
