CREATE TABLE finanzas (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY, -- Identificador único automático
    nombre VARCHAR2(100) NOT NULL, -- Nombre del empleado
    apellido VARCHAR2(100) NOT NULL, -- Apellido del empleado
    numero_empleado VARCHAR2(20) NOT NULL, -- Número de empleado
    correo VARCHAR2(100) NOT NULL, -- Correo electrónico
    telefono VARCHAR2(15), -- Teléfono del empleado
    fecha_contratacion DATE NOT NULL, -- Fecha de contratación
    salario NUMBER(10, 2) NOT NULL, -- Salario del empleado
    genero VARCHAR2(10), -- Género del empleado
    estado VARCHAR2(50), -- Estado actual del empleado (activo, inactivo, etc.)
    PRIMARY KEY (id) -- Llave primaria
);

